            <p>Known as lean, sizzurp, purple drink and hi-tech (among other names) the drug-fueled drink of Promethazine Codeine cough syrup mixed with soda or candy has been referenced in over 30 percent of rap songs that reached the top ten of Billboard’s Hot 100 in 2017 – including <a href="https://genius.com/artists/Migos" data-api_path="/artists/44080">Migos</a> <a href="https://genius.com/Migos-bad-and-boujee-lyrics" data-api_path="/songs/2845980">“Bad and Boujee” featuring Lil Uzi Vert</a> and <a href="https://genius.com/artists/Future" data-api_path="/artists/2197">Future</a>’s <a href="https://genius.com/Future-mask-off-lyrics" data-api_path="/songs/2989266">“Mask Off.”</a></p>

<p>Although its lyrical references have now reached a broader population, lean culture can be traced back decades to Houston. Lean burst onto the music scene through the prolific chopped and screwed movement in the ‘80s and '90s, pioneered by <a href="https://genius.com/artists/Dj-screw" data-api_path="/artists/5367">DJ Screw</a>. It then trickled down to popular radio hits in the early aughts, most notably with <a href="https://genius.com/artists/Lil-wayne" data-api_path="/artists/4">Lil Wayne</a>.</p>

<p>Beyond its status in Houston’s lifestyle, the drink has fueled addiction and is believed to have contributed to the deaths of some of hip-hop’s greats. Check out the video above to learn more about lean’s cultural influence on rap music.
