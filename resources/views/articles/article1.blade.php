          <div class="article_rich_text_formatting">
       <p>A decade ago, as the world was being ushered into the “blog era,” a special DIY scene was developing in Chicago. Local artists weren’t getting much national coverage <a href="https://genius.com/a/how-a-new-generation-of-chicago-artists-redefined-rap" data-api_path="/articles/4064">unless they were named</a> <a href="https://genius.com/artists/Kanye-west" data-api_path="/artists/72">Kanye West</a>, <a href="https://genius.com/artists/Lupe-fiasco" data-api_path="/artists/147">Lupe Fiasco</a>, <a href="https://genius.com/artists/Common" data-api_path="/artists/223">Common</a>, or <a href="https://genius.com/artists/Twista" data-api_path="/artists/74">Twista</a>—but the new creators did their own thing, unconcerned with the approval of the industry. While hot hip-hop rookies from elsewhere ventured to Chicago to little fanfare, rising Chicago artists were packing events and parties across the city.</p>

<p>Artists like <a href="https://genius.com/artists/The-cool-kids" data-api_path="/artists/1331">The Cool Kids</a>, <a href="https://genius.com/artists/Treated-crew" data-api_path="/artists/21792">Treated Crew</a> (Mano, HXLT, Mic Terror &amp; more), <a href="https://genius.com/artists/Kid-sister" data-api_path="/artists/15127">Kid Sister</a>, <a href="https://genius.com/artists/Kidz-in-the-hall" data-api_path="/artists/7129">Kidz in the Hall</a>, Boardroom and Flosstradamus were setting trends in fashion and sound. Many of these new acts were meshing hip-hop with juke—a form of house music native to Chicago. The Cool Kids—with their futuristic retro style of music and dress—began to gain attention from the industry at large. Their videos showed an alternative side to Chicago, which repped local streetwear brands, vintage sneakers, BMX bikes (“Dyno with the black mag”) and mopeds. Meanwhile, Kidz In The Hall noted the emerging scene on “Go Ill” saying:</p>

<blockquote class="g_lyrics_tag">

   <div class="referent referent--white">
     In Hyde Park after dark<br>
Down 53rd blowing herb with University nerds
   </div>

</blockquote>
<p>Elsewhere in the city, streetwise Southside emcees Bump J, GLC, Mikkey Halsted, and Sly Polaroid were mainstays on the mixtape circuit and were gaining traction on sites like Myspace. Bump J enjoyed mainstream success in 2005 after signing a reported $1 million dollar record deal with Atlantic Records on the heels of Chicago’s 2004 rap boom. However, his rap dreams were temporarily cut short after severing ties with Atlantic Records and <a href="https://www.fakeshoredrive.com/2009/08/bump-j-takes-federal-plea-for-2007-bank-robbery.html/">getting caught up in the street life</a>. But before all that, on “Nothing’s Gonna Stop Me” he rapped about getting love in his city:</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    Chicago gotta love me man<br>
It’s packed I’m on stage<br>
Squashed the beef<br>
Got a couple tracks from Kanye
  </div>

</blockquote>





<p>The L.E.P. Bogus Boys seemingly picked up from where Bump J left off, as they started gaining traction with their straight-to-YouTube videos—their most notable being “Goin In For The Kill”—from their 2010 release <em>Don’t Feed Da Killaz Vol. 3</em>. The videos, which included gritty yet cinematic shots and epic storylines began to get MTV airplay, and helped the duo eventually score a record deal with Interscope. Seemingly every national artist who came through Chicago recorded with the duo, who would break up in late 2013.</p>

<p>No doubt watching the successful DIY approach of the Bogus Boys, and their use of YouTube as a promotional tool, a new crop of young artists would emerge that would change the face of Chicago music forever. The genre would eventually be known nationally as Drill—a slang term created by late L.E.P. Bogus Boys affiliate Pac Man. The drill sound could best be described as Chicago’s spin on trap music, but often times including darker subject matter. It was reality rap in the truest sense—and gave the world a bird’s eye view into the sometimes grim circumstances of the Chicago streets.</p>

<p>King Louie became Drill’s first breakout star by racking up hundreds of thousands of views on YouTube and mixtape site DatPiff. His 2011 mixtape <em>Chiraq Drillinois</em> was widely recognized as the first public use of the controversial term Chiraq. But he stilll summed up his perspective on his city on songs like “Live and Die in Chicago” rapping:</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    I was made in Chicago, I got paid in Chicago<br>
Out of town getting counted profuse amounts then it’s back to Chicago<br>
Been to Paris, LA, NY to A to me they all great but they not like Chicago
  </div>

</blockquote>





<p>But it was a younger artist who would blow the doors off the drill scene, the then 16-year-old Chief Keef. Thanks to early-viral hits such as “300” and “I Don’t Like,” the polarizing upstart Keef would secure a record deal with Interscope worth an estimated $6 million dollars. Keef would get so massive that Kanye West remixed Keef’s hit “I Don’t Like,” adding his magic to the record as well as high profile features from Pusha T, Jadakiss, and Big Sean. Kanye was paying close attention to the scene, recruiting both Louie and Keef to appear on his 2013 album <em>Yeezus</em>, which infused elements made popular by the drill sound.</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    Chief Keef, King Louie, this is Chi, right?
  </div>

</blockquote>





<p>The industry was also watching Chicago closely as major labels flocked to Chicago to find the next big thing. <a href="https://genius.com/artists/Lil-durk" data-api_path="/artists/21964">Lil Durk</a> and <a href="https://genius.com/artists/Lil-reese" data-api_path="/artists/20186">Lil Reese</a> were quickly scooped up by Def Jam, where Chicago native No I.D. sat as Executive Vice President. When the dust settled, close to a dozen artists from Rockie Fresh (MMG) to YP (Universal) to Spenzo (Atlantic), all secured record deals. Even indie artists like Tree, Project Mayhem, Vic Spencer and Chris Crack were gaining national exposure via national press outlets like MTV and Pitchfork. But on Durk’s first official single on Def Jam “Dis Ain’t What U Want,” he opened up about a problem almost every street artist in Chicago faced (and still face): they couldn’t perform in their own city.</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    I can’t do no shows cause I terrify my city<br>
They say I terrify my city
  </div>

</blockquote>





<p>The threat of violence and the crowd it would attract kept Chicago promoters at bay, with local law enforcement pulling the plug on shows from any drill artists at venues across the city. Chief Keef was even banned from performing in hologram form in neighboring Hammond, Indiana. Proving despite their talent and success, they still were feared by the CPD and local media. So much so Lil Durk declared, “Fuck TMZ, fuck Breaking News and ABC.”</p>

<p>The scene may seemed male dominated, but artists like <a href="https://genius.com/artists/Sasha-go-hard" data-api_path="/artists/34047">Sasha Go Hard</a> and <a href="https://genius.com/artists/Katie-got-bandz" data-api_path="/artists/63184">Katie Got Bandz</a> were making a lot of noise as well. Katie would spawn the local hit “Pop Out,” which still shuts down clubs to this day. <a href="https://genius.com/artists/Tink" data-api_path="/artists/20064">Tink</a>, who broke out on the scene with “Fingers Up,” would eventually catch the attention of super producer Timbaland. <a href="https://genius.com/artists/Dreezy" data-api_path="/artists/41226">Dreezy</a> would sign to Interscope Records where she dropped her debut album that included the platinum single “Body,” featuring fellow Chicagoan <a href="https://genius.com/artists/Jeremih" data-api_path="/artists/278">Jeremih</a>. But before all that, she got over a million views on her “Chiraq” remix rapping:</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    Hit up Harolds on 87th<br>
For a 5 piece and some mild sauce
  </div>

</blockquote>





<p>While the drill scene was grabbing a bulk of the national headlines and media attention surrounding Chicago, there was another movement happening in the city that was about to reach a fever pitch. SaveMoney was a collection of talented artists from all corners of the city, who each had a unique style and presence. While many of the artists were from the same streets as the drill artists, they rapped about the city from a different perspective. They were only teenagers at the time, but this crew would change the Chicago hip-hop scene forever.</p>

<p>Vic Mensa was touted as the golden child at an early age thanks in part to his affiliation with Naledge’s Brainiac Society. But once he joined a band called <a href="https://genius.com/artists/Kids-these-days" data-api_path="/artists/13070">Kids These Days</a>, which became one of the most popular acts in the city, he was helping sell out venues like the historical Metro. Sadly, Kids These Days would break up as a group and Vic Mensa would go back to being a solo artist, which he kicked off with his heralded INNANETAPE, followed by Chicago house influenced “Down On My Luck,” which would chart around the globe. The success lead to big collabs with Kanye West (“U Mad,” “Wolves”) as well as a deal with Jay-Z’s Roc Nation label, where he’d drop his debut album in 2017. On his debut, he recalled growing up the Chi slightly differently than his drill counterparts on songs like “Memories on 47th St.” where he rapped:</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    Gunshots outside my window, drug deals out by the Citgo<br>
But mama always made sure the tooth fairy found my pillow<br>
My pops was always workin', he put the family first<br>
Chicago Saturdays in the park and Sundays at church
  </div>

</blockquote>





<p>Waiting in the wings as Vic began his rise was Chance The Rapper. Chance made a splash locally with his <em>10 Day</em> mixtape, but he rose to the top of the local rap scene so quickly that by the time his <em>Acid Rap</em> project dropped in 2013, he became a national name. Major labels heavily courted the hometown hero, but sticking to his guards, Chance and his longtime manager Pat Corcoran decided to forge a path independently. A decision that would change the game. Chance would become such a beloved hometown hero, he openly wondered about running for mayor on “Somewhere In Paradise”</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    Chancelor the Rapper, Chatham the hood<br>
Santa to children, had to bag up the goods<br>
They say I’m savin my city, say I’m staying for good<br>
They screamin ‘Chano for mayor,’ I’m thinkin maybe I should
  </div>

</blockquote>





<p>At that point, the SaveMoney box logo stickers could be seen all over the city. Joey Purp would become a critical darling thanks to his <em>iiiDrops</em> project. Towkio would make waves, literally, with his .Wav Theory project, and link up with Rick Rubin, who is set to executive produce his debut album. Crew members Caleb James, Brian Fresco, and KAMI all released their own acclaimed projects. Donnie Trumpet made his history alongside Chance the Rapper and their group the Social Experiment by having the first-ever free project on iTunes, their 2015 release <em>Surf</em>. SaveMoney brought light to a changing Chicago, as Joey Purp reflected on “Corners” rapping:</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    Now up in the corners where killers used to inhabit<br>
They built a row of new condos where they tore up project buildings
  </div>

</blockquote>





<p>Elsewhere in the city, <a href="https://genius.com/artists/G-herbo" data-api_path="/artists/32397">G Herbo</a> and <a href="https://genius.com/artists/Lil-bibby" data-api_path="/artists/33238">Lil Bibby</a> (“If you lookin’ for us, just pull up on Essex” – G Herbo) had the streets on fire thanks to anthems such as “Kill Shit” and “My Hood.” Herbo would gain the attention of Nicki Minaj, who quickly flew the artist out to LA to appear on her track “Chiraq” (“I’m in Hollywood, came from Kingston Foods.”) Herb’s brand of street knowledge and exquisite lyricism helped his debut album, <em>Humble Beast</em>, move 20,000 copies in its first week in 2017. Lil Bibby would gain the attention of Dr. Luke who quickly signed him to his Kemosabe Records imprint as he went on to record songs with the likes of T.I., Future, Juicy J and R. Kelly. His debut album is slated for a 2018 release.</p>

<p>Youth organizations such as Young Chicago Authors and YouMedia, helped groom a slew of the new artists from Chicago. The spoken scene would help launch stars such as <a href="https://genius.com/artists/Saba" data-api_path="/artists/35599">Saba</a>, <a href="https://genius.com/artists/Mick-jenkins" data-api_path="/artists/34513">Mick Jenkins</a>, and <a href="https://genius.com/artists/Noname" data-api_path="/artists/48449">Noname</a>. Noname has been one of the most cherished and critically acclaimed artists in the city, by perfectly blending rap and poetry. Her 2016 debut album <em>Telefone</em> graced the top spots on many year end lists. Noname’s delivery was effortless as she vividly described her memories and life in Chicago on “Diddy Bop” spitting:</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    Summertime, city life, Chi-town, my town, my town<br>
After school matters like I’m needing that stipend right now<br>
Kennicott parking lot got caught with the blunt like “wow wow”
  </div>

</blockquote>





<p>Mick Jenkins has also found a great deal of success, touring the globe after his 2014 release <em>The Water[s]</em> broke through nationally. Mick offered a unique look at the Windy City, with dizzying wordplay that highlighting the broken systems and racial disparity of the city, but also the beauty of the nation’s third largest city. On his “Bootleg Kev Freestyle” he rapped:</p>

<blockquote class="g_lyrics_tag">

  <div class="referent referent--white">
    I got a thesis or slick dissertation<br>
Our entire species is young and impatient and passive<br>
All at the same damn time, why?<br>
Chi to kill them with kindness
  </div>

</blockquote>





<p>Famous Dex, Valee, Kweku Collins, Femdot, The Boy Illinois, Lucki, Supa Bwe, Queen Key, and Cdot Honcho are a few of the artists who are heating Chicago up at this very moment, and are the new class to usher the city into its next phase.</p>
