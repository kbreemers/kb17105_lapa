          <h1 class="mt-4">How A New Generation Of Chicago Artists Redefined Rap</h1>

          <!-- Author -->
          <p class="lead">
            by
            <a href="https://www.facebook.com/roberts.apsusals">Roberts Apšusals</a>
          </p>

          <hr>

          <!-- Date/Time -->
          <p>Posted on January 14, 2018 at 12:00 PM</p>

          <hr>

          <!-- Preview Image -->
          <img class="img-fluid rounded" src="https://scontent-arn2-1.xx.fbcdn.net/v/t1.0-9/13095827_1003439296414801_3330812785131695305_n.jpg?oh=59c38c367855c5aff78d0d1924e686cc&oe=5AF5B0DE" alt="">

